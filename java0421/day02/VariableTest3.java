/*
算数运算符：+  -   +  -  *   /   %   （前）++  （后）++  （前）--   （后）--     + （连接符）
*/
class VariableTest3 {
	public static void main(String[] args) {
		int a = 12;
		int b = 5;

		//除法
		int res = a / b; //
		System.out.println(res);

		System.out.println(1234 / 1000 * 1000);

		//取模（求余）
		System.out.println(a % b);

		System.out.println(1 % 5);
	}
}
